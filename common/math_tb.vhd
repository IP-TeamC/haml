LIBRARY ieee;
USE ieee.std_logic_1164.ALL;
USE ieee.numeric_std.ALL;

use work.math.ALL;
use work.util.ALL;

ENTITY math_tb IS
END math_tb;

ARCHITECTURE behavior OF math_tb IS
BEGIN

    test_fp_mul: process
        variable a : signed(35 downto 0);
        variable b : signed(35 downto 0);
        variable res : signed(35 downto 0);
    begin

        -- mal 0
        a := "0" & "0000000000000000000000000" & "0000000000";
        b := "111111111111111111111111111111111111";
        res := fp_mul(a, b, 10);
        assert res = "000000000000000000000000000000000000";

        -- mal 1
        a := "0" & "0000000000000000000000001" & "0000000000";
        b := "000000000000000000000110100000000001";
        res := fp_mul(a, b, 10);
        assert res = "000000000000000000000110100000000001";

        -- mal 2
        a := "0" & "0000000000000000000000010" & "0000000000";
        b := "000000000000000000000110100000000001";
        res := fp_mul(a, b, 10);
        assert res = "000000000000000000001101000000000010";

        -- mal 0,5
        a := "0" & "0000000000000000000000000" & "1000000000";
        b := "000000000000000000000110100000000001";
        res := fp_mul(a, b, 10);
        assert res = "000000000000000000000011010000000000";

        -- mal -1
        a := "1" & "1111111111111111111111111" & "0000000000";
        b := "000000000000000000000110100000000001";
        res := fp_mul(a, b, 10);
        assert res = "111111111111111111111001011111111111";

        -- mal -1 reverse
        a := "1" & "1111111111111111111111111" & "0000000000";
        b := "111111111111111111111001011111111111";
        res := fp_mul(a, b, 10);
        assert res = "000000000000000000000110100000000001";

        -- Quadrat
        a := "00000000000000" & x"984" & "0000000000";
        b := "00000000000000" & x"984" & "0000000000";
        res := fp_mul(a, b, 10);
        assert res = "000" & x"5A8C10" & "0000000000";

        -- Quadrat negativ
        a := "00000000000000100110000100" & "0000000000";
        b := "11111111111111011001111100" & "0000000000";
        res := fp_mul(a, b, 10);
        assert res = "11101001010111001111110000" & "0000000000";

        wait;
    end process;

END;
